using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApppractice1
{
    internal class loop
    {
        public static void forloop()
        {
            for (int x = 1; x != 10; x++)
            {
                Console.WriteLine(x);
            }
        }

    
    public static void While()
    {
        int x = 2;
        while (x<10)
        {
            Console.WriteLine("x in while:"+x);
            x++;
        }
     }
     }
